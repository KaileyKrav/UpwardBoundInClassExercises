#Displays numbers 1 through 5 using different for loops


print("Display numbers 1 through 5")

#Create a list of 5 numbers
list = [1, 2, 3, 4, 5]
for num in list:
    print(num)

print('\n')

#Use a list of 5 numbers
for num in [1, 2, 3, 4, 5]:
    print(num)

print('\n')

#Use the range function
for num in range(5):
    print(num)

print('\n')
